name = "vasista"

#length of a string
print(len(name))

#substrings (indiv. string)
print(name[0])
print(name[6])
print(name[-2])

#substrings (actual substrings)
print(name[0:-2])
print(name[0:4])
print(name[:3])
print(name[2:5])

#returns whole string
print(name[::])

#step sizes
print(name[::2])

#reverse a string
print(name[::-1])
